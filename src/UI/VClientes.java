    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        
        
  
        //se comunica con la CL para averiguar si el numero de cedula que se 
        //está introduciendo ya existe o no en la BD
          try {
              unCliente= clpdv.buscarPorCedula(txtCedula.getText());
          } catch (PDVException ex) {
              Logger.getLogger(VClientes.class.getName()).log(Level.SEVERE, null, ex);
          }
          //Si la cedula no existe entonces unCliente va a ser igual a null
          //y se le indica que debe ingresar al procedimiento para agregar un
          //nuevo cliente
        if( txtCedula.getText().equals("") || txtNombre.getText().equals("") || txtAlias.getText().equals("") || txtTelefonoUno.getText().equals("") || txtTelefonoDos.getText().equals("") || txtEmail.getText().equals("") || txtDireccion.getText().equals("") || txtReferidoPor.getText().equals("")) {
            JOptionPane.showMessageDialog(null,"No pueden Haber campos vacíos","Atención",JOptionPane.ERROR_MESSAGE);
        }else{
          if(unCliente==null){
            try {
                crearCliente();
            } catch (PDVException ex) {
                Logger.getLogger(VClientes.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            //caso contrario significa que ese numero de cedula que se ha introducido
            //ya existe en la BD
            JOptionPane.showMessageDialog(null,"El Cliente con cedula ["+ txtCedula.getText() + "] Ya Existe en la Base de Datos, Favor Verificar!!!","Atención",JOptionPane.ERROR_MESSAGE);
            limpiar();
        }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

