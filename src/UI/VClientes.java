    public void limpiar(){
        txtCedula.setText("");
        txtNombre.setText("");
        txtAlias.setText("");
        txtTelefonoUno.setText("");
        txtTelefonoDos.setText("");
        txtDireccion.setText("");
        txtEmail.setText("");
        txtReferidoPor.setText("");
        txtCedula.grabFocus();
    }
    public void crearCliente()throws PDVException{
      
            try{
            
            unCliente=clpdv.buscarPorCedula(txtCedula.getText());
            } catch (PDVException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            }
            if(unCliente==null){
                clpdv.crearCliente(txtCedula.getText(),
                                   txtNombre.getText(),
                                   txtAlias.getText(),
                                   txtTelefonoUno.getText(),
                                   txtTelefonoDos.getText(),
                                   txtEmail.getText(),
                                   txtDireccion.getText(),
                                   txtReferidoPor.getText());
                JOptionPane.showMessageDialog(null,"Cliente Guardado Satisfactoriamente!!","Atención",JOptionPane.INFORMATION_MESSAGE);
                limpiar();
                buscarCodCliente();
            }else{
                JOptionPane.showMessageDialog(null,"El Cliente con cedula ["+ txtCedula.getText() +"] Ya Existe favor Verificar!!!","Atención",JOptionPane.ERROR_MESSAGE);
                limpiar();
            }
        
        
    }
