    private void btnDevolverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDevolverActionPerformed
        // TODO add your handling code here:
        float totalVenta;
        float precioVenta;
        float totalEfectivo;
        float subTotal;
        int cantActual;
        int cantDevuelta;
        int cantNueva;
        DecimalFormat formato = new DecimalFormat("#,###.00"); 
        
    try {
        Number nTotalVenta=java.text.NumberFormat.getInstance().parse(txtTotalVenta.getText());
        totalVenta=nTotalVenta.floatValue();
        
        Number nPrecioVenta=java.text.NumberFormat.getInstance().parse(txtPrecioVenta.getText());
        precioVenta=nPrecioVenta.floatValue();
        cantDevuelta=(int)spCantidadDevuelta.getValue();
        subTotal=(cantDevuelta*precioVenta);
        cantActual=Integer.parseInt(txtCantActual.getText());
        totalEfectivo=(totalVenta-subTotal);
        cantNueva=cantActual+(int)spCantidadDevuelta.getValue();
        txtTotalVenta.setText(formato.format(totalEfectivo));
        
        actualizarInventario(txtIdArticulo.getText(),cantNueva);
        txtIdArticulo.setText("");
        txtArticulo.setText("");
        spCantidadDevuelta.setValue(0);
        txtPrecioVenta.setText("");
        txtIdArticulo.requestFocus();
        
    } catch (ParseException | PDVException ex) {
        Logger.getLogger(VVentasConsignacion.class.getName()).log(Level.SEVERE, null, ex);
    }
        
        
        
    }//GEN-LAST:event_btnDevolverActionPerformed
    
