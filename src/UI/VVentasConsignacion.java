    public void buscarNoFactura(){
        int IdFactura;
        String tipoVenta="";
        String totalVentConsig;
        DecimalFormat formato = new DecimalFormat("#,###.00");
        float totalVenta=0;
        String cliente;
        
        IdFactura=Integer.parseInt(txtNoFactura.getText());
        
        ArrayList<Ventas> ventas= new ArrayList<>();
        
        try {
            ventas= clpdv.buscarVentasConsignacion(IdFactura);
            
        } catch (PDVException ex) {
            Logger.getLogger(VVentasConsignacion.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (ventas==null){
           /* JOptionPane.showMessageDialog(null,"La Factura No. " + IdFactura + " No existe en la Base de Datos, favor verificar","Atenci√≥n",JOptionPane.ERROR_MESSAGE);
            txtNoFactura.setText("");
            txtNoFactura.requestFocus();*/
        }else{
           
          for(int i=0;i<ventas.size();i++){
              tipoVenta=ventas.get(i).getTipoVenta();
          }
          
          if(tipoVenta.equals("CERRADA")){
              JOptionPane.showMessageDialog(null,"El No.Factura a consignacion " + IdFactura + " ya fue cerrada y no puede ser modificada!!!","Atenci√≥n",JOptionPane.ERROR_MESSAGE);
              txtNoFactura.setText("");
              txtNoFactura.requestFocus();
          
          }else{
              if(tipoVenta.equals("CONSIGNACION")){
                
                for(int i=0; i <ventas.size();i++){
                    try { 
                    
                        Number nTotal = java.text.NumberFormat.getInstance().parse(ventas.get(i).getSubTotal());
                        float total=nTotal.floatValue();
                        totalVenta=totalVenta+total;
                        cliente=ventas.get(i).getCliente();
                        txtCliente.setText(cliente);
                        txtTotalVenta.setText(formato.format(totalVenta));
                        txtIdArticulo.requestFocus();
                    }catch (ParseException ex) {
                        Logger.getLogger(VVentasConsignacion.class.getName()).log(Level.SEVERE, null, ex);
                    }
               
                
                
            }
       
        }else{
              JOptionPane.showMessageDialog(null,"El No Factura buscado no es de Tipo Consignaci√≥n, Favor Verificar","Atenci√≥n",JOptionPane.ERROR_MESSAGE);
              txtNoFactura.setText("");
              txtNoFactura.requestFocus();
        }
        }
        }
    }
