    public void crearCuenta(){
        //MultiAccount multiAccount = new MultiAccount();
        
        try{
            String user = txtUser.getText();
            // Obtener el password
            char passArray[] = txtPassword.getPassword();
            char passConfirmArray[] = txtPassConfirm.getPassword();
            // Revisar que sean letras y numeros
            for (int i = 0; i < passArray.length; i++) {
            char c = passArray[i];
            // Si no es letra o numero entonces no es valido
            if (!Character.isLetterOrDigit(c)) 
            {
                JOptionPane.showMessageDialog(null,"El password debe llevar al menos una letra y un numero");
            }

        }
        // Convertir el password a String
        String pass = new String(passArray);
        String passConfirm= new String(passConfirmArray);
        if(passConfirm.equals(pass))
        {
            clpdv.crear(user,pass);
            JOptionPane.showMessageDialog(null,"Usuario y Clave Creados Satisfactoriamente!!");
            dispose();
          
        }else{
            JOptionPane.showMessageDialog(null,"Las Contrase√±as no coinciden, Digite de nuevo las contrase√±as","Error",JOptionPane.ERROR_MESSAGE);
            txtPassword.setText("");
            txtPassConfirm.setText("");
            txtPassword.grabFocus();
          
        }
            
        
        }catch (java.lang.Exception e)
        {
            System.out.println(e.getMessage());
        }
        txtPassword.grabFocus();
    
    }//GEN-LAST:event_btoRegistrarActionPerformed

